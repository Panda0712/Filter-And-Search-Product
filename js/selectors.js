export function getButtonWrapper() {
  return document.querySelector("#buttons");
}

export function getButtonList() {
  return document.querySelectorAll(".button-value");
}

export function getSearchButton() {
  return document.getElementById("search");
}

export function getSearchInput() {
  return document.getElementById("search-input");
}

export function getProductWrapper() {
  return document.getElementById("products");
}

export function getProductList() {
  return document.querySelectorAll(".card");
}

